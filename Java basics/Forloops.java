public class Forloops {
    public static void main(String[] args) {
        for (int i=1; i<=10; i++){
            System.out.println(i);
        }
        //print odd no.
        int n = 5;
        for (int i=0; i<n; i++){
            System.out.println(2*i+1);
       }
        //using decrement
        for (int i=5; i>0; i--){
            System.out.println(i);
        }
    }

    
} 