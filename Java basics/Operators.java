public class Operators {
    public static void main(String[] args) {
        int a = 4;
        int b = 6 % a;        //Module operator it will print remainder when 6/4
        b += 3;               //It will add 3 in the result
        b *= 0;               // It will multiply the result by 0
        System.out.println(b);
        System.out.println(b==0);  //It is comparison operator and will return bollean
        System.out.println(64>5 && 64<5);   //logical operator if any of the value is false it will print false
        System.out.println(64>5 || 64<5);   //logical operator it meanss or if any of the value is True it will print True
        
       
    }
    
}
 